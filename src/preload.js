const { contextBridge, ipcRenderer } = require('electron');

contextBridge.exposeInMainWorld('api', {
  getStats: () => ipcRenderer.invoke('system:getStats'),
  getDrives: () => ipcRenderer.invoke('system:getDrives'),
  getNetwork: () => ipcRenderer.invoke('system:getNetwork'),
  ping: (target) => ipcRenderer.invoke('network:ping', target),
  trace: (target) => ipcRenderer.invoke('network:trace', target),
  dns: (target) => ipcRenderer.invoke('network:dns', target),
  flushDns: () => ipcRenderer.invoke('quick:flushDns'),
  restartExplorer: () => ipcRenderer.invoke('quick:restartExplorer'),
  clearTemp: () => ipcRenderer.invoke('quick:clearTemp'),
  openDeviceManager: () => ipcRenderer.invoke('quick:openDeviceManager'),
  batteryReport: () => ipcRenderer.invoke('quick:batteryReport'),
  killHung: () => ipcRenderer.invoke('quick:killHung'),
  scanLargeFiles: (options) => ipcRenderer.invoke('disk:scanLargeFiles', options),
  listProcesses: () => ipcRenderer.invoke('process:list'),
  killProcess: (pid) => ipcRenderer.invoke('process:kill', pid),
  listStartup: () => ipcRenderer.invoke('startup:list'),
  disableStartup: (name, command) => ipcRenderer.invoke('startup:disable', name, command),
  enableStartup: (name) => ipcRenderer.invoke('startup:enable', name),
  getNotes: () => ipcRenderer.invoke('notes:get'),
  setNotes: (text) => ipcRenderer.invoke('notes:set', text),
  getClipboard: () => ipcRenderer.invoke('clipboard:getHistory'),
  clearClipboard: () => ipcRenderer.invoke('clipboard:clear'),
  pinClipboard: (id, pinned) => ipcRenderer.invoke('clipboard:pin', id, pinned),
  deleteClipboard: (id) => ipcRenderer.invoke('clipboard:delete', id),
  writeClipboard: (text) => ipcRenderer.invoke('clipboard:write', text),
  tagClipboard: (id, tags) => ipcRenderer.invoke('clipboard:tag', id, tags),
  expireClipboard: (id, minutes) => ipcRenderer.invoke('clipboard:expire', id, minutes),
  setClipboardSettings: (settings) => ipcRenderer.invoke('clipboard:setSettings', settings),
  minimize: () => ipcRenderer.invoke('window:minimize'),
  maximize: () => ipcRenderer.invoke('window:maximize'),
  close: () => ipcRenderer.invoke('window:close'),
  notify: (title, body) => ipcRenderer.invoke('app:notify', title, body),
  getAutostart: () => ipcRenderer.invoke('app:getAutostart'),
  setAutostart: (enabled) => ipcRenderer.invoke('app:setAutostart', enabled),
  checkUpdates: () => ipcRenderer.invoke('app:checkUpdates'),
  getVersion: () => ipcRenderer.invoke('app:getVersion'),
  getPaths: () => ipcRenderer.invoke('app:getPaths'),
  onUpdateReady: (cb) => ipcRenderer.on('app:updateReady', cb),
});
